# Define variables
x = 15  # Integer
price = 9.99  # Float
discount = 0.2
name = "Rolf"  # String

# Perform a calculation
result = price * (1 - discount)  # Calculate discounted price

# Print result
print(result)  # Outputs: 7.992

# Reassign a variable
name = "Bob"
print(name)  # Outputs: Bob

# String operation
print(name * 2)  # Outputs: BobBob

# Variable references
a = 25
b = a
print(a)  # Outputs: 25
print(b)  # Outputs: 25
b = 17
print(a)  # Outputs: 25
print(b)  # Outputs: 17